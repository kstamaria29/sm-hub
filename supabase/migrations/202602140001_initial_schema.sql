-- Initial v1 scaffold schema for Family Hub.
-- Focus: planning-level tables + RLS intent stubs.

create extension if not exists pgcrypto;

create schema if not exists app;

create type public.family_role as enum ('owner', 'admin', 'member');
create type public.invite_status as enum ('pending', 'accepted', 'revoked', 'expired');
create type public.room_type as enum ('chat', 'game');
create type public.game_status as enum ('pending', 'active', 'finished', 'cancelled');

create table public.families (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  created_by uuid not null references auth.users (id),
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table public.family_members (
  id uuid primary key default gen_random_uuid(),
  family_id uuid not null references public.families (id) on delete cascade,
  user_id uuid not null references auth.users (id) on delete cascade,
  role public.family_role not null default 'member',
  status text not null default 'active' check (status in ('active', 'left', 'removed')),
  joined_at timestamptz not null default timezone('utc', now()),
  created_at timestamptz not null default timezone('utc', now()),
  unique (family_id, user_id)
);

create table public.invites (
  id uuid primary key default gen_random_uuid(),
  family_id uuid not null references public.families (id) on delete cascade,
  invited_by uuid not null references auth.users (id),
  invitee_contact text,
  token text not null unique,
  status public.invite_status not null default 'pending',
  expires_at timestamptz not null,
  accepted_by uuid references auth.users (id),
  accepted_at timestamptz,
  created_at timestamptz not null default timezone('utc', now())
);

create table public.rooms (
  id uuid primary key default gen_random_uuid(),
  family_id uuid not null references public.families (id) on delete cascade,
  kind public.room_type not null,
  slug text not null,
  title text not null,
  created_by uuid not null references auth.users (id),
  created_at timestamptz not null default timezone('utc', now()),
  unique (family_id, slug)
);

create table public.messages (
  id bigint generated by default as identity primary key,
  family_id uuid not null references public.families (id) on delete cascade,
  room_id uuid not null references public.rooms (id) on delete cascade,
  sender_id uuid not null references auth.users (id),
  content text not null,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default timezone('utc', now())
);

create table public.games (
  id uuid primary key default gen_random_uuid(),
  family_id uuid not null references public.families (id) on delete cascade,
  room_id uuid not null references public.rooms (id) on delete cascade,
  created_by uuid not null references auth.users (id),
  mapping_id text not null default 'classic_v1',
  status public.game_status not null default 'pending',
  current_turn_user_id uuid references auth.users (id),
  winner_user_id uuid references auth.users (id),
  started_at timestamptz,
  finished_at timestamptz,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table public.game_players (
  id uuid primary key default gen_random_uuid(),
  game_id uuid not null references public.games (id) on delete cascade,
  family_id uuid not null references public.families (id) on delete cascade,
  user_id uuid not null references auth.users (id),
  player_order int not null check (player_order > 0),
  tile_position int not null default 1 check (tile_position between 1 and 100),
  token_avatar_pack_id uuid,
  created_at timestamptz not null default timezone('utc', now()),
  unique (game_id, user_id),
  unique (game_id, player_order)
);

create table public.game_events (
  id bigint generated by default as identity primary key,
  game_id uuid not null references public.games (id) on delete cascade,
  family_id uuid not null references public.families (id) on delete cascade,
  event_type text not null,
  payload jsonb not null default '{}'::jsonb,
  created_by uuid references auth.users (id),
  created_at timestamptz not null default timezone('utc', now())
);

create table public.user_profiles (
  user_id uuid primary key references auth.users (id) on delete cascade,
  family_id uuid not null references public.families (id) on delete cascade,
  display_name text,
  avatar_style_id text,
  cinematics_enabled boolean not null default true,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table public.avatar_packs (
  id uuid primary key default gen_random_uuid(),
  family_id uuid not null references public.families (id) on delete cascade,
  user_id uuid not null references auth.users (id) on delete cascade,
  style_id text not null,
  version int not null check (version > 0),
  status text not null default 'ready' check (status in ('queued', 'processing', 'ready', 'failed')),
  base_path text not null,
  created_by uuid references auth.users (id),
  created_at timestamptz not null default timezone('utc', now()),
  unique (family_id, user_id, style_id, version)
);

