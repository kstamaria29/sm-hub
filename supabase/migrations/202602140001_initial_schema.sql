create or replace function app.touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = timezone('utc', now());
  return new;
end;
$$;

create trigger trg_families_touch_updated_at
before update on public.families
for each row execute function app.touch_updated_at();

create trigger trg_games_touch_updated_at
before update on public.games
for each row execute function app.touch_updated_at();

create trigger trg_user_profiles_touch_updated_at
before update on public.user_profiles
for each row execute function app.touch_updated_at();

create or replace function app.is_family_member(target_family_id uuid)
returns boolean
language sql
stable
as $$
  select exists (
    select 1
    from public.family_members fm
    where fm.family_id = target_family_id
      and fm.user_id = auth.uid()
      and fm.status = 'active'
  );
$$;

create or replace function app.family_role_for_user(target_family_id uuid)
returns public.family_role
language sql
stable
as $$
  select fm.role
  from public.family_members fm
  where fm.family_id = target_family_id
    and fm.user_id = auth.uid()
    and fm.status = 'active'
  limit 1;
$$;

